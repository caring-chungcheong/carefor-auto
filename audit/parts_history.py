# -*- coding: utf-8 -*-
"""정비내역 자유텍스트 → 부품 교체 이벤트(타이어·브레이크패드·배터리) 추출.

케어포 정비구분은 `고장수리/예방정비/정기점검/기타` 뿐이라 부품을 모른다.
부품은 **정비내역 자유텍스트**에만 있고, 형식이 정비소마다 제각각이다.

⚠️ 함정 — 단순 키워드 매칭은 과탐 5배다(실측 144건 → 실제 32건).
  · "엔진오일 **교체** ... 타이어 마모상태 점검" → '타이어'와 '교체'가 같은 줄에 있다는 이유로 잡힘.
    **엔진오일**을 간 거지 타이어가 아니다. → 부품과 동사의 **거리**를 봐야 한다.
  · "타이어 마모상태 점검"은 분기 상용구라 압도적으로 많다(273건). 이걸 교체로 세면 실제 교체가 묻힌다.
  · "타이어 **교체시기 도래** 곧 **교체예정**" → 미래 예정이지 교체가 아니다.
⚠️ 반대로 너무 좁혀도 놓친다(실측):
  · "타이어 **4짝** 교체" · "브레이크패드 **전열 2짝** 교체수리" — 사이에 한글 수량어가 낀다.
"""
from __future__ import annotations

import re

# 부품 표기 흔들림(실측): 브레이크패드/브레이크 패드, 배터리/밧데리
PARTS = {
    "타이어": r"타이어",
    "브레이크패드": r"브레이크\s*패드",
    "배터리": r"배터리|밧데리",
}
# ⚠️ 부품과 동사 사이를 **화이트리스트로 못박으면 안 된다** — 정비소 문체가 하나 늘 때마다
#    조용히 놓친다. 실측 누락: '브레이크 패드 및 디스크 교환' · '타이어, 얼라이먼트, 쇼바, 노우암 교체'
#    · '후방 브레이크 패드 닳음 발견, 교체' · '앞타이어의 심한 마모가 발견되어 우선 교체'
#    → **아무 글자나 허용**하되, 사이에 **다른 부품이 끼면 거부**하는 네거티브 규칙으로 뒤집는다.
GAP = 22   # 부품 뒤 이 글자수 안에 동사가 와야 한다
VERB = r"(교체|교환|장착)"
# 사이에 이게 끼면 **그 동사는 이 부품 것이 아니다**(실측: '타이어 상태 양호, 엔진오일 교체' →
# 엔진오일을 간 거지 타이어가 아니다). 함께 교체되는 부속(디스크 등)은 넣지 않는다.
OTHER = (r"엔진\s*오일|엔진오일|미션\s*오일|오일|에어컨\s*필터|에어\s*필터|필터|냉각수|부동액|와이퍼|"
         r"전구|램프|등\b|벨트|모터|히터|워터펌프|요소수|"
         # 타이어에 붙어 있지만 타이어가 아닌 부속 — '타이어공기주입구 교환' 은 밸브를 간 것이다
         r"공기\s*주입구|주입구|밸브|캡")
# 미래·권고 표현 — 동사 뒤에 오면 실제 교체가 아니다(실측: '교체시기 도래', '교체예정', '교체 필요')
# ⚠️ `^\s*` 필수 — tail 은 동사 뒤부터라 **공백으로 시작한다**. 이게 없으면 '교체필요'(붙임)만
#    걸러지고 '교체 필요'(띄움)는 통과한다. 실측 오탐: '타이어 앞, 뒤 모두 교체 필요(2月중_)'
#    → 1월 '예정'과 2월 실제 교체가 이중 계상됐다.
# '교체권유 하심' · '교환 권유' 는 정비소 권고지 실제 교체가 아니다(실측 2건).
FUTURE = r"^\s*(시기|예정|필요|권장|권유|요망|바람|주기|하시|하심)"
# '타이어 마모상태 점검' 류 상용구 — 부품 바로 뒤가 점검이면 교체 아님
INSPECT = r"^[\s]*(마모상태|공기압|상태)?[\s]*점검"


# ── 수리 vs 점검 판정 ─────────────────────────────────────────────
# ⚠️ 케어포 '정비구분' 필드로는 못 나눈다 — 월례 점검이 대부분 **예방정비(325건)**로 등록돼 있고
#    '정기점검'은 38건뿐이다. 결국 내역 텍스트로 판단해야 한다.
# 점검 상용구(분기마다 그대로 복사됨)
BOILER = [r"차량\s*외부\s*점검", r"엔진\s*오일\s*점검", r"타이어\s*마모상태\s*점검",
          r"타이어\s*공기압\s*점검", r"라이트\s*점검\s*(\([^)]*\))?", r"브레이크\s*점검",
          r"냉각수\s*점검", r"와이퍼\s*점검", r"무상\s*점검", r"차량\s*점검"]
# ⚠️ '엔진오일 **필요교체** km 190,678km' 는 **점검 메모**지 교체가 아니다.
#    이걸 안 지우면 월례 점검 대부분이 '수리'로 둔갑한다.
# ⚠️ 이름 주의 — 위쪽 FUTURE(부품 판정용)와 **다른 것**이다. 같은 이름을 쓰면 덮어써서
#    검증 끝난 find_parts 가 조용히 깨진다(실제로 깨뜨렸다).
# '엔진오일 **교체 주기** 확인 (74,500km)일때 교체 필요' 는 점검 메모다 — `교체 주기` 도 지운다.
# ※ '엔진오일 교체 현재 96,627km 다음 교체 주기 104,627km' 는 앞의 '엔진오일 교체' 가 남아 True 유지.
REPAIR_FUTURE = [r"필요\s*교체", r"교체\s*필요", r"교체\s*시기", r"교체\s*예정", r"교체\s*주기",
                 r"교체km", r"필요교체"]
ACT = r"교체|교환|수리|장착|보충|충전|용접|도색|재생|보수|탈착"


def is_repair(text: str) -> bool:
    """실제 수리·교체가 있었던 건인가? (점검 상용구·미래형을 지운 뒤에도 행위 동사가 남는가)

    앱 정비이력에는 **수리는 전부**, 점검은 **점검표(첨부)가 있는 것만** 올린다(사용자 방침).
    실측 분류: 수리 205건 · 점검 347건(그중 첨부 보유 25건) → 앱 등록 230건.
    """
    t = (text or "").replace("‎", "")
    for p in BOILER + REPAIR_FUTURE:
        t = re.sub(p, " ", t)
    return bool(re.search(ACT, t))


def find_parts(text: str) -> list[dict]:
    """정비내역에서 실제 부품 교체 이벤트만 뽑는다 → [{part, evidence}, ...]

    판단: 부품 키워드 뒤 짧은 창(수량어 허용) 안에 교체 동사가 오고,
         그 동사가 미래형(예정·필요)이 아니며, 부품 바로 뒤가 '점검' 상용구가 아닐 것.
    """
    t = (text or "").replace("\u200E", "")   # 케어포 내역에 LRM 제어문자가 섞여 있다
    out, seen = [], set()
    for part, kw in PARTS.items():
        for m in re.finditer(kw, t):
            tail = t[m.end():m.end() + GAP]
            if re.match(INSPECT, tail):
                continue
            v = re.search(VERB, tail)
            if not v:
                continue
            # 사이에 다른 부품이 끼면 그 동사는 이 부품 것이 아니다
            # (실측: '타이어 상태 양호, 엔진오일 교체' → 엔진오일을 간 것)
            if re.search(OTHER, tail[:v.start()]):
                continue
            if re.match(FUTURE, tail[v.end():]):
                continue
            if part in seen:      # 한 건에 같은 부품이 여러 번 나와도 이벤트는 1개
                continue
            seen.add(part)
            out.append({"part": part, "evidence": t[m.start():m.end() + 20].strip()})
    return out
